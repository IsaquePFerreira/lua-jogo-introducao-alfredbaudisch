-- Comentários de uma linha em lua começam com  "--"

print("Olá, lua!") -- A função print pra exibir algo na tela

-- String se escrevem entre aspas duplas ou simples
print('Oi, de novo!')

-- Números
print(1)
print(13.5)
print(-1)

-- Operadores Aritméticos

-- Adição
print(1 + 1)

-- Subtração
print(2 - 2)

-- Multiplicação
print(3 * 3)

-- Divisão
print(1 / 4)

-- Módulo
print(1 % 5)

-- Operadores Relacionais

-- Maior que
print("2 é maior que 3?")
print(2 > 3)

-- Menor que
print("2 é menor que 3?")
print(2 < 3)

-- Maior ou igual
print("2 é maior ou igual 3?")
print(2 >= 3)

-- Menor ou igual
print("2 é menor ou igual 3?")
print(2 <= 3)

-- Igualdade
print("2 é igual 3?")
print(2 == 3)

